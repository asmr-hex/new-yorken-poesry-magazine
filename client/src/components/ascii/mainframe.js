// readouts should only contain max 4 elements
const getComputeCycle = (readoutFrames=[], sequence=[]) => {
  const defaultReadout = String.raw`
    ▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫
    ▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫
    ▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫
    ▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫
    ▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫
    ▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫▫`

  if (sequence.length === 0) {
    sequence = range(0, readoutFrames.length)
  }
  
  const readouts = map(
    sequence,
    idx => readoutFrames[idx],
  )
  
  const period = 4
  const n = readouts.length
  const paddingSize = (n % period === 0 ? 0 : 1) * (period - (n % period))
  const padding = new Array(paddingSize).fill(defaultReadout)
  const paddedReadOuts = readouts.concat(padding)
  const getFrame = [
    getFrame1,
    getFrame2,
    getFrame3,
    getFrame4,
  ]

  return map(
    paddedReadOuts,
    (readout, idx) => {
      const frameIdx = idx % period
      const readoutRows = readout.toString().trim().split(/\r?\n/)
      
      return getFrame[frameIdx](readoutRows)
    },
  )
}

